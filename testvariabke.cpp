#include<iostream>
#include<cmath>
using namespace std;
int main(){
	int a,b,z;
	cout<<"Input value A : ";
	cin>>a;
	cout<<"Input value B : ";
	cin>>b;
	cout<<"Input value Z : ";
	cin>>z;
	cout<<"A + B + Z = "<<a+b+z<<endl;
	cout<<a <<"+" <<b<<"+" <<z <<"="<<a+b+z<<endl;
	cout<<"A ^ B = "<< pow(a,b)<<endl;
	cout<<"sqrt(z)="<<sqrt(z)<<endl;
	cout<<"Value of A = "<<a<<endl;
	cout<<"Value of B = "<<b<<endl;
	cout<<"Value of Z = "<<z<<endl;
	
		
	
	return 0;
}
